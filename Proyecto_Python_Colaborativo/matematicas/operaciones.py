def sumar(a, b):
    """Suma dos números."""
    return a + b

def restar(a, b):
    """Resta dos números."""
    return a - b

def multiplicar(a, b):
    """Multiplica dos números."""
    return a * b

def dividir(a, b):
    """Divide dos números. Lanza un error si el divisor es cero."""
    return a / b

def potencia(a, b):
    """Eleva un numero a a una potencia b. Lanza un error si el divisor es cero."""
    return a ** b